#include "bsp_i2c_gpio.h"



/*
*********************************************************************************************************
*	函 数 名: i2c_Delay
*	功能说明: I2C总线位延迟，最快400KHz
*	形    参：无
*	返 回 值: 无
*********************************************************************************************************
*/
static void i2c_Delay(void)
{
	uint8_t i;

	/*　
		可用逻辑分析仪测量I2C通讯时的频率
    工作条件：CPU主频180MHz ，MDK编译环境，1级优化
  
		经测试，循环次数为20~250时都能通讯正常

	*/
	for (i = 0; i < 30; i++);
}

/*
*********************************************************************************************************
*	函 数 名: i2c_Start
*	功能说明: CPU发起I2C总线启动信号
*	形    参：无
*	返 回 值: 无
*********************************************************************************************************
*/
void i2c_Start(void)
{
	/* 当SCL高电平时，SDA出现一个下跳沿表示I2C总线启动信号 */
	SDA_OUT();
	I2C_SDA = 1;
	I2C_SCL = 1;
	bsp_DelayUS(4);
	I2C_SDA = 0;
	bsp_DelayUS(4);
	I2C_SCL = 0;
//	i2c_Delay();  //
}

/*
*********************************************************************************************************
*	函 数 名: i2c_Start
*	功能说明: CPU发起I2C总线停止信号
*	形    参：无
*	返 回 值: 无
*********************************************************************************************************
*/
void i2c_Stop(void)
{
	/* 当SCL高电平时，SDA出现一个上跳沿表示I2C总线停止信号 */
	SDA_OUT();
	I2C_SCL = 0;
	I2C_SDA = 0;
	bsp_DelayUS(4);
	I2C_SDA = 1;
	I2C_SCL = 1;
	bsp_DelayUS(4);  //
}

/*
*********************************************************************************************************
*	函 数 名: i2c_WaitAck
*	功能说明: CPU产生一个时钟，并读取器件的ACK应答信号
*	形    参：无
*	返 回 值: 返回0表示正确应答，1表示无器件响应
*********************************************************************************************************
*/
uint8_t i2c_WaitAck(void)
{
	uint8_t re;
    
	SDA_IN();
	I2C_SDA = 1;	/* CPU释放SDA总线 */
	bsp_DelayUS(1);
	I2C_SCL = 1;	/* CPU驱动SCL = 1, 此时器件会返回ACK应答 */
	bsp_DelayUS(1);
    while(I2C_SDA_READ)
	{
		re++;
		if(re > 250)
		{
			i2c_Stop();
			return 1;
		}
			
	}
	I2C_SCL = 0 ;
	return 0;
}

/*
*********************************************************************************************************
*	函 数 名: i2c_Ack
*	功能说明: CPU产生一个ACK信号
*	形    参：无
*	返 回 值: 无
*********************************************************************************************************
*/
void i2c_Ack(void)
{
	I2C_SDA = 0;	/* CPU驱动SDA = 0 */
	bsp_DelayUS(2);
	I2C_SCL = 0;	/* CPU产生1个时钟 */
	i2c_Delay();
	I2C_SCL = 1;
	bsp_DelayUS(2);
	I2C_SDA = 0;	/* CPU释放SDA总线 */
}

/*
*********************************************************************************************************
*	函 数 名: i2c_NAck
*	功能说明: CPU产生1个NACK信号
*	形    参：无
*	返 回 值: 无
*********************************************************************************************************
*/
void i2c_NAck(void)
{
	I2C_SDA = 0;	/* CPU驱动SDA = 1 */
	SDA_OUT();
	I2C_SDA = 1;
	bsp_DelayUS(2);
	I2C_SCL = 1;	/* CPU产生1个时钟 */
	bsp_DelayUS(2);
	I2C_SCL = 0;	
}

/*
*********************************************************************************************************
*	函 数 名: i2c_SendByte
*	功能说明: CPU向I2C总线设备发送8bit数据
*	形    参：_ucByte ： 等待发送的字节
*	返 回 值: 无
*********************************************************************************************************
*/
void i2c_SendByte(uint8_t _ucByte)
{
	uint8_t i;
    
	SDA_OUT();
	I2C_SCL = 0;
    for(i = 0;i<8;i++)
	{
		I2C_SDA = (_ucByte&0x80)>>7;
		_ucByte<<=1;
		bsp_DelayUS(2);
		I2C_SCL = 1;
		bsp_DelayUS(2);
		I2C_SCL = 0;
		bsp_DelayUS(2);
	}
	
}


/*
*********************************************************************************************************
*	函 数 名: i2c_ReadByte
*	功能说明: CPU从I2C总线设备读取8bit数据
*	形    参：无
*	返 回 值: 读到的数据
*********************************************************************************************************
*/
uint8_t i2c_ReadByte(u8 ack)
{
	uint8_t i;
	uint8_t value;
	value = 0;
	SDA_IN();
	for (i = 0; i < 8; i++)
	{
		I2C_SCL = 0;
		bsp_DelayUS(2);
		I2C_SCL = 1;
		value <<= 1;
		if(I2C_SDA_READ)value++;
		bsp_DelayUS(1);
		
	}
	if(!ack)
		i2c_NAck();
	else
		i2c_Ack();
	return value;
}


/*
*********************************************************************************************************
*	函 数 名: i2c_CfgGpio
*	功能说明: 配置I2C总线的GPIO，采用模拟IO的方式实现
*	形    参：无
*	返 回 值: 无
*********************************************************************************************************
*/
void i2c_CfgGpio(void)
{
	GPIO_InitTypeDef GPIO_InitStructure;

	RCC_AHB1PeriphClockCmd(I2C_GPIO_CLK, ENABLE);	/* 打开GPIO时钟 */

	GPIO_InitStructure.GPIO_Pin = I2C_SCL_PIN | I2C_SDA_PIN;
	GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;
	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_OUT;  	
	GPIO_InitStructure.GPIO_OType = GPIO_OType_PP;  	/* 开漏输出 */
	GPIO_InitStructure.GPIO_PuPd = GPIO_PuPd_UP;
	GPIO_Init(I2C_GPIO_PORT, &GPIO_InitStructure);

	/* 给一个停止信号, 复位I2C总线上的所有设备到待机模式 */
	i2c_Stop();
}


/*
*********************************************************************************************************
*	函 数 名: i2c_CheckDevice
*	功能说明: 检测I2C总线设备，CPU向发送设备地址，然后读取设备应答来判断该设备是否存在
*	形    参：_Address：设备的I2C总线地址
*	返 回 值: 返回值 0 表示正确， 返回1表示未探测到
*********************************************************************************************************
*/
uint8_t i2c_CheckDevice(uint8_t _Address)
{
	uint8_t ucAck;

//	i2c_CfgGpio();		/* 配置GPIO */
	
	i2c_Start();		/* 发送启动信号 */

	/* 发送设备地址+读写控制bit（0 = w， 1 = r) bit7 先传 */
	i2c_SendByte(_Address | EEPROM_I2C_WR);
	ucAck = i2c_WaitAck();	/* 检测设备的ACK应答 */

	i2c_Stop();			/* 发送停止信号 */

	return ucAck;
}
